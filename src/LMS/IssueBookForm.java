    private void memIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_memIDKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
            int mid=Integer.parseInt(memID.getText());
            
            try
            {
               st=conn.prepareStatement("select * from members where ID=?");
               st.setInt(1,mid);
               rs=st.executeQuery();
               
               if(rs.next()==false)
               {
                   JOptionPane.showMessageDialog(this,"Member ID not found!");
               }
               else
               {
                   String membername = rs.getString("Name");
                   memName.setText(membername.trim());
               }
            }
               catch (SQLException ex) 
               {
            Logger.getLogger(IssueBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
            
    }//GEN-LAST:event_memIDKeyPressed

