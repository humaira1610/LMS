    private void addToDatabaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToDatabaseActionPerformed
        // TODO add your handling code here:
        int id=Integer.parseInt(memID.getText());
        String mname=memName.getText();
        int bID=Integer.parseInt(bookID.getText());
        
        SimpleDateFormat date1=new SimpleDateFormat("yyyy-MM-dd");
        String issueDate=date1.format(dateIssued.getDate());
        
        SimpleDateFormat date2=new SimpleDateFormat("yyyy-MM-dd");
        String returnDate=date2.format(dateReturned.getDate());
        
        
       
        
        try {
            st= conn.prepareStatement("insert into issuedbooks(memberID,memberName,bookID,issueDate,returnDate)values(?,?,?,?,?)");
            st.setInt(1,id);
            st.setString(2,mname);
            st.setInt(3,bID);
            st.setString(4,issueDate);
            st.setString(5,returnDate);
            int k = st.executeUpdate();
            
            if (k==1){
                JOptionPane.showMessageDialog(this,"Book issued.");
                //title.requestFocus();
                //author.requestFocus();
                //publisher.requestFocus();
                //pubYear.requestFocus();
                IssuedBooks ib=new IssuedBooks();
                this.hide();
                ib.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this,"Error");
            }
            
          
            
        } catch (SQLException ex) {
            Logger.getLogger(IssueBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_addToDatabaseActionPerformed

